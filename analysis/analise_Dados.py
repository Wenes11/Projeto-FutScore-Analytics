#Onde os dados foram coletados seram tratados e analisados
#Como tratar os dados coletados?
#1. Limpeza dos dados: Remover dados inconsistentes ou ausentes.
#2. Transformação dos dados: Normalizar ou padronizar os dados para análise.
#3. Análise exploratória: Utilizar estatísticas descritivas para entender os dados.
#4. Visualização: Criar gráficos e dashboards para apresentar os dados de forma clara.